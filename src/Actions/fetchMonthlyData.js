async function fetchMonthlyData() {
  let allResponses = [];
  symbolList.forEach(async (symbol) => {
    const response = await axios.get(
      `https://www.alphavantage.co/query?function=TIME_SERIES_MONTHLY&symbol=${symbol}&apikey=YLZUAWZUSRWKBI9C`
    );
    if (response.status === 200 && response.data) {
      let data = {};
      data[symbol] = response.data['Monthly Time Series'];
      allResponses.push(data);
    }
  });

  const columnData = sampleData
    .map((data) => {
      const symbol = Object.keys(data)[0];
      const allColumnHeaders = Object.keys(data[symbol]).slice(
        0,
        TOTAL_MONTHS + 1
      );
      return allColumnHeaders.map((headerList) => {
        const headerKey = moment(headerList).format('MMMM-yyyy');
        return {
          Header: headerKey,
          accessor: `${headerKey}_price`,
          Cell: ({ value }) => <Cell value={value} />,
        };
      });
    })[0]
    .reverse();
  columnData.unshift({
    Header: 'Stock',
    accessor: `symbol`,
  });
  return {
    columns: columnData,
    data: sampleData.map((data) => {
      let allData = {};
      Object.keys(data).forEach((columnHeader) => {
        let dataObj = {};
        dataObj['symbol'] = columnHeader;
        Object.keys(data[columnHeader]).forEach((subColumn) => {
          const headerKey = moment(subColumn).format('MMMM-yyyy');

          dataObj[`${headerKey}_price`] = {
            open: data[columnHeader][subColumn]['1. open'],
            high: data[columnHeader][subColumn]['2. high'],
            low: data[columnHeader][subColumn]['3. low'],
            close: data[columnHeader][subColumn]['4. close'],
            volume: data[columnHeader][subColumn]['5. volume'],
          };
        });
        allData = dataObj;
      });
      return allData;
    }),
  };
  //   return allResponses;
}

export default fetchMonthlyData;
